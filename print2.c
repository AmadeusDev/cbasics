#include <stdio.h>

int main(void)
{
    printf("%06i %06u %06x %6x %06f %06E %06G\n\n", -5, -5, 200, 200, 10.0,
	   10.0, 10.0);

    printf("%*s %1s %6s %%06.2G\n", 10, "%*i", "%06.2f", "%06.0E");

    printf("%*i %06.2f %06.0E %06.2G\n", 10, -5, 10.0 / 3, 10.0 / 3,
	   10.0 / 3);
    printf("\n%.8s %0*.*f\n", "Posledni vystup:", 10, 4, 10.0 / 3);
    return 0;
}

